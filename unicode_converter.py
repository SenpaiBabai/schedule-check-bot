def converter(symbol):
    # only russian letters
    if symbol == 'А':
        return '%u0410' 
    elif symbol == 'Б':
        return '%u0411'
    elif symbol == 'В':
        return '%u0412'
    elif symbol == 'Г':
        return '%u0413'
    elif symbol == 'Д':
        return '%u0414'
    elif symbol == 'Е':
        return '%u0415'
    elif symbol == 'Ё':
        return '%u0401'
    elif symbol == 'Ж':
        return '%u0416'
    elif symbol == 'З':
        return '%u0417'
    elif symbol == 'И':
        return '%u0418'
    elif symbol == 'Й':
        return '%u0419'
    elif symbol == 'К':
        return '%u041A'
    elif symbol == 'Л':
        return '%u041B'
    elif symbol == 'М':
        return '%u041C'
    elif symbol == 'Н':
        return '%u041D'
    elif symbol == 'О' or symbol == 'о':
        return '%u041E'
    elif symbol == 'П':
        return '%u041F'
    elif symbol == 'Р':
        return '%u0420'
    elif symbol == 'С':
        return '%u0421'
    elif symbol == 'Т':
        return '%u0422'
    elif symbol == 'У':
        return '%u0423'
    elif symbol == 'Ф':
        return '%u0424'
    elif symbol == 'Х':
        return '%u0425'
    elif symbol == 'Ц':
        return '%u0426'
    elif symbol == 'Ч':
        return '%u0427'
    elif symbol == 'Ш':
        return '%u0428'
    elif symbol == 'Щ':
        return '%u0429'
    elif symbol == 'Ы':
        return '%u042B'
    elif symbol == 'Э':
        return '%u042D'
    elif symbol == 'Ю':
        return '%u042E'
    elif symbol == 'Я':
        return '%u042F'
